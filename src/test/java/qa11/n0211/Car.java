package qa11.n0211;

public class Car {
    private String pickUpPlace;
    private String make;
    private String model;
    private String year;
    private String engine;
    private String doors;
    private String seats;
    private String autoclass;
    private String fuelConsumption;
    private String serialNumber;
    private String price;
    private String distance;

    public Car setPickUpPlace(String pickUpPlace) {
        this.pickUpPlace = pickUpPlace;
        return this;
    }

    public Car setMake(String make) {
        this.make = make;
        return this;
    }

    public Car setModel(String model) {
        this.model = model;
        return this;
    }

    public Car setYear(String year) {
        this.year = year;
        return this;
    }

    public Car setEngine(String engine) {
        this.engine = engine;
        return this;
    }

    public Car setDoors(String doors) {
        this.doors = doors;
        return this;
    }

    public Car setSeats(String seats) {
        this.seats = seats;
        return this;
    }

    public Car setAutoclass(String autoclass) {
        this.autoclass = autoclass;
        return this;
    }

    public Car setFuelConsumption(String fuelConsumption) {
        this.fuelConsumption = fuelConsumption;
        return this;
    }

    public Car setSerialNumber(String serialNumber) {
        this.serialNumber = serialNumber;
        return this;
    }

    public Car setPrice(String price) {
        this.price = price;
        return this;
    }

    public Car setDistance(String distance) {
        this.distance = distance;
        return this;
    }

    public String getPickUpPlace() {
        return pickUpPlace;
    }

    public String getMake() {
        return make;
    }

    public String getModel() {
        return model;
    }

    public String getYear() {
        return year;
    }

    public String getEngine() {
        return engine;
    }

    public String getDoors() {
        return doors;
    }

    public String getSeats() {
        return seats;
    }

    public String getAutoclass() {
        return autoclass;
    }

    public String getFuelConsumption() {
        return fuelConsumption;
    }

    public String getSerialNumber() {
        return serialNumber;
    }

    public String getPrice() {
        return price;
    }

    public String getDistance() {
        return distance;
    }
}
